    <p class="w-full">
      Domis enterijeri Shop funkcioniše sa dve grupe kupaca: maloprodajni ili MP
      KUPCI i veleprodajni ili VP KUPCI. Razlika u načinu poručivanja za ove
      grupe kupaca je u tome što se VP kupci mogu upotrebom Korisničkog imena i
      Lozinke u prodavnicu registrovati odmah, a MP kupci to čine tek u KORAKU
      3. Do ove razlike dolazi jer VP kupci podatke za svoj pristut dobijaju
      direktno od Administratora.
      <br /><br />
      <span class="font-bold">KORAK 1: ODABIR PROIZVODA </span>
      <br />
      U ovom koraku, nakon što ste odabrali željeni proizvod iz padajućeg menija
      koji se nalazi u zaglavlju između logotipa i dela za pretragu proizvoda, potrebno
      je da kliknete na dugme „Kupi“, nakon čega će željeni proizvod biti dodat u
      deo „Vaša korpa“ koji se nalazi u desnom uglu zaglavlja. Klikom na „Vaša korpa“
      možete videti stanje naručenih proizvoda i opcije „Korpa“ i „Plaćanje“ se pojaviti
      prozoru u kojem ćete videti opcije za nastavak kupovine ili završavanje narudžbine.
      <br /><br />
      Ukoliko želite proverite stanje naručenih proizvoda ili promenite poručenu
      količinu kliknite na „Korpa“. Prelaskom na ovu stranicu, na njenoj levoj strani,
      stičete uvid u naziv poručenog proizvoda, njegovu cenu, naručenu količinu i
      ukupan iznos. Na ovoj stranici možete manipulisati proizvodom tako što ćete
      ga izbaciti iz poružbine (odustati od poručivanja proizvoda) ili promeniti
      količinu koju naručujete. Potvrdu korigovanog stanja proizvoda koji se naleze
      u korpi potvrđujete klikom na dugme „Ažuriraj korpu“ koje se nalazi ispod liste
      naručenih proizvoda.
      <br /><br />
      Na desnoj strani ove stranice nalaze se podaci o Vrednosti poružbine koji obuhvataju
      cenu porudžbine i pripadajući PDV (za VP kupce). Na delu ove stranice možete
      nastaviti kupovinu klikom na „Nastavite kupovinu“ ili ukoliko želite da završite
      narudžbinu, to možete učiniti klikom na dugme „Nastavite plaćanje“ i tako prelazite
      na sledeći korak.
      <br /><br />
      <span class="font-bold"
        >KORAK 2: PORUČIVANJE Na ovoj stranici imate uvid u detalje</span
      >
      <br />
      svoje porudžbine. Sa desne strane ispod „Vaša porudžbina“ nalaze se informacije
      koje obuhvataju naziv poručenih proizvoda, izabranu količinu, iznos porudžbine
      i iznos pripadajućeg PDV-a (za VP kupce).
      <br /><br />
      Sa leve strane nalazi se formular sa podacima o kupcu. Ukoliko ste prijavljeni
      na korisnički nalog vaši podaci će se nalaziti u formularu, a ukoliko niste,
      potrebno je da ga pounite. Pređimo sada na objašnjenje polja tog formulara.
      <br /><br />
      Polja za unošenje „Imena“ i „Prezimena“ su obavezna, dok je popunjavanje polja
      „Ime pravnog lica“ dato kao opciono. Njega popunjavate samo u slučaju da sa
      sajta poručujete kao pravno lice. Ostala polja koja je potrebno popuniti i
      bez čijeg popunjavanja nije moguće završiti porudžbinu su: Država, Adresa,
      Gradska opština/grad, Region, Poštanski broj, Telefon i Email adresa. Ispod
      formulara se nalazi deo u kome možete nam napisati posebne napomene koje se
      odnose na vašu porudžbinu, a koje nismo predvideli ponuđenim poljima formulara.
      Ovde možete primetiti i opciju „Napravite nalog“ čijom verifikacijom omogućavate
      našem sistemu da zapamti vaše podatke te ih nećete morati ponovo ukucavati
      prilikom neke naredne kupovine.
      <br /><br />
      Nakon što popunite sva polja, potrebno je označiti polje „Prihvatam uslove
      korišćenja“, koje se nalazi u desnom donjem uglu ispod ukupnog iznosa vaše
      porudžbine i načina plaćanja, a zatim i kliknuti na dugme „Naručite“.
